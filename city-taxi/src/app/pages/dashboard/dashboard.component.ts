  ngOnInit(): void {
    this.loadData();
  }

  loadData(){
    this.firebaseService.getData('drivers').subscribe(data => {
      this.driversCount = data.length;
      console.log('loadData - drivers:', data);
    });
    this.firebaseService.getData('passengers').subscribe(data => {
      this.passengersCount = data.length;
      console.log('loadData - passengers:', data);
    });
    this.firebaseService.getData('reservations').subscribe(data => {
      this.reservationsCount = data.length;
      console.log('loadData - passengers:', data);
    });
  }
